function newPosition = movement(currentPosition)
input=getKeyboardInput();
if isequal(input,'rightarrow')
    newPosition=currentPosition;
end
if isequal(input,'leftarrow')
    newPosition=currentPosition;
end
if isequal(input,'uparrow')
    newPosition=currentPosition;
end
if isequal(input,'downarrow')
    newPosition=currentPosition;
end
end